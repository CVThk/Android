package cvt.main.webservice;

/**
 *
 * @author Chau Thinh
 */
public class SinhVien {
    protected String name;
    protected int age;

    public SinhVien(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
    
}
